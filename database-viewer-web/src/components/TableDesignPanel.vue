function normalizeTypeKey(value: string) {
  return value.replace(/\s+/g, ' ').trim().toLowerCase();
}

function providerCatalog(provider: ProviderType) {
  return PROVIDER_TYPE_CATALOG[provider] ?? [];
}

function findTypeEntry(provider: ProviderType, rawType: string) {
  const normalized = normalizeTypeKey(rawType);
  return providerCatalog(provider).find((entry) => normalizeTypeKey(entry.value) === normalized || normalizeTypeKey(entry.base) === normalized) ?? null;
}

function inferEditorMode(provider: ProviderType, baseType: string, length: string, precision: string, scale: string): TypeEditorMode {
  const entry = findTypeEntry(provider, baseType);
  if (entry) {
    return entry.mode;
  }

  if (precision && scale) {
    return 'precision-scale';
  }

  if (precision) {
    return 'precision';
  }

  if (scale) {
    return 'scale';
  }

  if (length.toLowerCase() === 'max') {
    return 'length-or-max';
  }

  if (length) {
    return 'length';
  }

  return 'none';
}

function buildTypeInput(baseType: string, mode: TypeEditorMode, length: string, precision: string, scale: string) {
  const base = baseType.trim();
  if (!base) {
    return '';
  }

  if (mode === 'precision-scale' && precision.trim()) {
    return `${base}(${precision.trim()}${scale.trim() ? `,${scale.trim()}` : ''})`;
  }

  if (mode === 'precision' && precision.trim()) {
    return `${base}(${precision.trim()})`;
  }

  if (mode === 'scale' && scale.trim()) {
    return `${base}(${scale.trim()})`;
  }

  if ((mode === 'length' || mode === 'length-or-max') && length.trim()) {
    return `${base}(${length.trim()})`;
  }

  return base;
}

function parseTypeInput(provider: ProviderType, rawInput: string, fallback?: Partial<EditableColumn>) {
  const trimmed = rawInput.trim();
  const match = /^([^()]+?)(?:\(([^)]*)\))?$/.exec(trimmed);
  const baseType = match?.[1]?.trim() || trimmed || fallback?.typeBase || '';
  const args = (match?.[2] ?? '').split(',').map((item) => item.trim()).filter(Boolean);
  const entry = findTypeEntry(provider, trimmed || baseType);

  let length = fallback?.length ?? '';
  let precision = fallback?.precision ?? '';
  let scale = fallback?.scale ?? '';

  if (args.length === 1) {
    const firstArg = args[0] ?? '';
    if (entry?.mode === 'precision') {
      precision = firstArg;
      scale = '';
      length = '';
    }
    else if (entry?.mode === 'scale') {
      scale = firstArg;
      precision = '';
      length = '';
    }
    else {
      length = firstArg;
      precision = '';
      scale = '';
    }
  }
  else if (args.length >= 2) {
    precision = args[0] ?? '';
    scale = args[1] ?? '';
    length = '';
  }
  else if (entry?.defaultLength !== undefined && !length && (entry.mode === 'length' || entry.mode === 'length-or-max')) {
    length = entry.defaultLength;
  }
  else if (entry?.defaultPrecision !== undefined && !precision && (entry.mode === 'precision' || entry.mode === 'precision-scale')) {
    precision = entry.defaultPrecision;
    scale = entry.defaultScale ?? scale;
  }
  else if (entry?.defaultScale !== undefined && !scale && entry.mode === 'scale') {
    scale = entry.defaultScale;
  }

  const mode = entry?.mode ?? inferEditorMode(provider, baseType, length, precision, scale);
  return {
    typeBase: baseType,
    typeInput: buildTypeInput(baseType, mode, length, precision, scale) || trimmed,
    length,
    precision,
    scale,
  };
}

function buildEditableColumns(columns: TableColumn[]) {
  const provider = design.value?.provider ?? 'sqlserver';
  return columns.map((column) => {
    const length = column.maxLength === null || column.maxLength === undefined
      ? ''
      : column.maxLength < 0
        ? 'max'
        : String(column.maxLength);
    const precision = column.numericPrecision === null || column.numericPrecision === undefined ? '' : String(column.numericPrecision);
    const scale = column.numericScale === null || column.numericScale === undefined ? '' : String(column.numericScale);
    const parsed = parseTypeInput(provider, buildTypeInput(column.type, inferEditorMode(provider, column.type, length, precision, scale), length, precision, scale), {
      typeBase: column.type,
      length,
      precision,
      scale,
    });

    return {
      draftKey: createEditableColumnDraftKey(column.name),
      originalName: column.name,
      originalComment: column.comment ?? null,
      name: column.name,
      typeBase: parsed.typeBase,
      typeInput: parsed.typeBase,
      length: parsed.length,
      precision: parsed.precision,
      scale: parsed.scale,
      comment: column.comment ?? '',
      isNullable: column.isNullable !== false,
      isPrimaryKey: !!column.isPrimaryKey,
      isAutoGenerated: !!column.isAutoGenerated,
      isComputed: !!column.isComputed,
      isNew: false,
    };
  });
}

