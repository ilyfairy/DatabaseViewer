function quoteIdentifier(provider: ProviderType, value: string) {
  if (provider === 'mysql') {
    return `\`${value.replace(/`/g, '``')}\``;
  }

  if (provider === 'postgresql' || provider === 'sqlite') {
    return `"${value.replace(/"/g, '""')}"`;
  }

  return `[${value.replace(/\]/g, ']]')}]`;
}

function quoteIndexName(provider: ProviderType, value: string) {
  return quoteIdentifier(provider, value);
}

function qualifiedTable(provider: ProviderType, schema: string | null | undefined, tableName: string) {
  return schema
    ? `${quoteIdentifier(provider, schema)}.${quoteIdentifier(provider, tableName)}`
    : quoteIdentifier(provider, tableName);
}

function buildCreateIndexSql() {
  const current = design.value;
  if (!current) {
    throw new Error('表设计尚未加载完成。');
  }

  const indexName = createIndexName.value.trim();
  if (!indexName) {
    throw new Error('索引名称不能为空。');
  }

  if (!createIndexColumns.value.length) {
    throw new Error('请至少选择一个索引列。');
  }

  if (current.indexes.some((item) => item.name.toLowerCase() === indexName.toLowerCase())) {
    throw new Error(`索引 ${indexName} 已存在。`);
  }

  const qualifiedColumns = createIndexColumns.value.map((column) => quoteIdentifier(current.provider, column)).join(', ');
  const uniqueKeyword = createIndexUnique.value ? 'UNIQUE ' : '';
  const tableName = qualifiedTable(current.provider, current.schema, current.name);

  return `CREATE ${uniqueKeyword}INDEX ${quoteIndexName(current.provider, indexName)} ON ${tableName} (${qualifiedColumns})`;
}

