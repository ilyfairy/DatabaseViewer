  saving.value = true;
  try {
    await store.executeTableDesignSql(props.tab.tableKey, sql, '表结构已更新');
  }
  catch (error) {
    store.showNotice('warning', error instanceof Error ? error.message : '表设计保存失败');
  }
  finally {
    saving.value = false;
  }
}

