function updateTypeMenuPosition() {
  const anchor = typeMenuAnchor.value;
  if (!anchor) {
    typeMenuPosition.value = null;
    return;
  }

  const rect = anchor.getBoundingClientRect();
  const width = Math.min(Math.max(Math.round(rect.width), 170), window.innerWidth - 20);
  const height = Math.min(196, Math.max(116, window.innerHeight - 120));
  let left = rect.left;
  if (left + width > window.innerWidth - 10) {
    left = window.innerWidth - width - 10;
  }
  left = Math.max(10, left);

  let top = rect.bottom + 4;
  if (top + height > window.innerHeight - 10) {
    top = Math.max(10, rect.top - height - 4);
  }

  typeMenuPosition.value = {
    top,
    left,
    width,
    height,
  };
}

function toggleTypeMenu(column: EditableColumn, event: MouseEvent) {
  if (!canEditColumn(column)) {
    return;
  }

  const key = columnRowKey(column);
  if (typeMenuRowKey.value === key) {
    typeMenuRowKey.value = null;
    typeMenuAnchor.value = null;
    typeMenuPosition.value = null;
    return;
  }

  commentEditorVisible.value = false;
  commentEditorTarget.value = null;
  commentEditorDraft.value = '';
  commentEditorAnchor.value = null;
