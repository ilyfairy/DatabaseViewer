function normalizeComment(comment: string | null | undefined) {
  return (comment ?? '').trim();
}

function sqlLiteral(value: string) {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlUnicodeLiteral(value: string) {
  return `N'${value.replace(/'/g, "''")}'`;
}

function buildCommentStatements(provider: ProviderType, schema: string | null | undefined, tableName: string, columnName: string, previousComment: string | null | undefined, nextComment: string | null | undefined) {
  const before = normalizeComment(previousComment);
  const after = normalizeComment(nextComment);
  if (before === after) {
    return [];
  }

  if (provider === 'sqlite') {
    if (after || before) {
      throw new Error('SQLite 当前不支持列注释。');
    }

    return [];
  }

  if (provider === 'postgresql') {
    const qualifiedColumn = `${qualifiedTable(provider, schema, tableName)}.${quoteIdentifier(provider, columnName)}`;
    return [`COMMENT ON COLUMN ${qualifiedColumn} IS ${after ? sqlLiteral(after) : 'NULL'}`];
  }

  if (provider === 'sqlserver') {
    const schemaName = schema ?? 'dbo';
    if (before && !after) {
      return [`EXEC sp_dropextendedproperty @name = N'MS_Description', @level0type = N'SCHEMA', @level0name = ${sqlUnicodeLiteral(schemaName)}, @level1type = N'TABLE', @level1name = ${sqlUnicodeLiteral(tableName)}, @level2type = N'COLUMN', @level2name = ${sqlUnicodeLiteral(columnName)}`];
    }

    const qualifiedObjectName = `[${schemaName.replace(/]/g, ']]')}].[${tableName.replace(/]/g, ']]')}]`;
    return [
      `DECLARE @table_object_id int = OBJECT_ID(${sqlUnicodeLiteral(qualifiedObjectName)});\nDECLARE @column_id int = COLUMNPROPERTY(@table_object_id, ${sqlUnicodeLiteral(columnName)}, 'ColumnId');\nIF EXISTS (SELECT 1 FROM sys.extended_properties WHERE class = 1 AND major_id = @table_object_id AND minor_id = @column_id AND name = N'MS_Description')\n    EXEC sp_updateextendedproperty @name = N'MS_Description', @value = ${sqlUnicodeLiteral(after)}, @level0type = N'SCHEMA', @level0name = ${sqlUnicodeLiteral(schemaName)}, @level1type = N'TABLE', @level1name = ${sqlUnicodeLiteral(tableName)}, @level2type = N'COLUMN', @level2name = ${sqlUnicodeLiteral(columnName)}\nELSE\n    EXEC sp_addextendedproperty @name = N'MS_Description', @value = ${sqlUnicodeLiteral(after)}, @level0type = N'SCHEMA', @level0name = ${sqlUnicodeLiteral(schemaName)}, @level1type = N'TABLE', @level1name = ${sqlUnicodeLiteral(tableName)}, @level2type = N'COLUMN', @level2name = ${sqlUnicodeLiteral(columnName)}`,
    ];
  }

  return [];
}

function buildAlterSql() {
  const current = design.value;
  if (!current) {
    throw new Error('表设计尚未加载完成。');
  }

  const tableName = qualifiedTable(current.provider, current.schema, current.name);
  const statements: string[] = [];

  for (const column of draftColumns.value) {
    applyTypeInput(column, column.typeInput);
    const name = column.name.trim();
    const typeDecl = columnTypeDeclaration(column);
    if (!name) {
      throw new Error('字段名不能为空。');
    }

    if (!typeDecl) {
      throw new Error(`字段 ${name} 的类型不能为空。`);
    }

    /* 类型名/参数直接拼入 SQL（不是标识符也不是字符串字面量），必须严格校验防止注入。
       类型名仅允许字母、数字、空格、下划线；参数仅允许数字或 'max'。 */
    if (!/^[A-Za-z_][A-Za-z0-9_ ]*$/.test(column.typeBase.trim())) {
      throw new Error(`字段 ${name} 的类型名 "${column.typeBase}" 包含非法字符。`);
    }

    const dimensionPattern = /^(?:\d+|max)?$/i;
    if (!dimensionPattern.test(column.length.trim())) {
      throw new Error(`字段 ${name} 的大小参数 "${column.length}" 包含非法字符，仅允许数字或 max。`);
    }

    if (!dimensionPattern.test(column.precision.trim())) {
      throw new Error(`字段 ${name} 的精度参数 "${column.precision}" 包含非法字符，仅允许数字。`);
    }

    if (!dimensionPattern.test(column.scale.trim())) {
      throw new Error(`字段 ${name} 的比例参数 "${column.scale}" 包含非法字符，仅允许数字。`);
    }
  }

  const duplicate = draftColumns.value
    .map((column) => column.name.trim().toLowerCase())
    .find((name, index, list) => list.indexOf(name) !== index);
  if (duplicate) {
    throw new Error(`存在重复字段名：${duplicate}`);
  }

  for (const draft of draftColumns.value) {
    const nextName = draft.name.trim();
    const nextType = columnTypeDeclaration(draft);
    const nextNullable = draft.isPrimaryKey ? false : draft.isNullable;

    if (draft.isNew) {
      if (current.provider === 'sqlite' && !draft.isNullable) {
        throw new Error('SQLite 新增字段当前只支持可空列。');
      }

      const commentSql = normalizeComment(draft.comment);
      const addStatement = current.provider === 'mysql'
        ? `ALTER TABLE ${tableName} ADD ${quoteIdentifier(current.provider, nextName)} ${nextType}${nextNullable ? ' NULL' : ' NOT NULL'}${commentSql ? ` COMMENT ${sqlLiteral(commentSql)}` : ''}`
        : `ALTER TABLE ${tableName} ADD ${quoteIdentifier(current.provider, nextName)} ${nextType}${nextNullable ? ' NULL' : ' NOT NULL'}`;
      statements.push(addStatement);
      statements.push(...buildCommentStatements(current.provider, current.schema, current.name, nextName, null, draft.comment));
      continue;
    }

    const original = current.columns.find((column) => column.name === draft.originalName);
    if (!original) {
      continue;
    }

    const nameChanged = original.name !== nextName;
    const typeChanged = original.type !== draft.typeBase
      || ((original.maxLength === null || original.maxLength === undefined ? '' : original.maxLength < 0 ? 'max' : String(original.maxLength)) !== draft.length)
      || ((original.numericPrecision === null || original.numericPrecision === undefined ? '' : String(original.numericPrecision)) !== draft.precision)
      || ((original.numericScale === null || original.numericScale === undefined ? '' : String(original.numericScale)) !== draft.scale);
    const nullableChanged = (original.isNullable !== false) !== draft.isNullable;
    const commentChanged = normalizeComment(original.comment) !== normalizeComment(draft.comment);
    if (!nameChanged && !typeChanged && !nullableChanged && !commentChanged) {
      continue;
    }

    if ((original.isPrimaryKey || original.isAutoGenerated || original.isComputed) && (nameChanged || typeChanged || nullableChanged)) {
      throw new Error(`字段 ${original.name} 当前不支持在设计页修改。`);
    }

    if (current.provider === 'mysql') {
      const commentSql = normalizeComment(draft.comment);
      statements.push(`ALTER TABLE ${tableName} CHANGE COLUMN ${quoteIdentifier(current.provider, original.name)} ${quoteIdentifier(current.provider, nextName)} ${nextType}${draft.isNullable ? ' NULL' : ' NOT NULL'}${commentSql ? ` COMMENT ${sqlLiteral(commentSql)}` : ''}`);
      continue;
    }

    if (current.provider === 'sqlite') {
      if (typeChanged || nullableChanged || commentChanged) {
        throw new Error('SQLite 设计页当前只支持重命名字段。');
      }

      if (nameChanged) {
        statements.push(`ALTER TABLE ${tableName} RENAME COLUMN ${quoteIdentifier(current.provider, original.name)} TO ${quoteIdentifier(current.provider, nextName)}`);
      }
      continue;
    }

    const resolvedName = nameChanged ? nextName : original.name;
    if (nameChanged) {
      if (current.provider === 'sqlserver') {
        /* sp_rename 的第一个参数是一个点分隔的对象名字符串，必须在字符串内部使用 [] 引用标识符，
           否则包含 . 或 ] 的 schema/table/column 名会被错误解析。 */
        const bracketQuote = (v: string) => `[${v.replace(/\]/g, ']]')}]`;
        const fullColumnName = `${bracketQuote(current.schema ?? 'dbo')}.${bracketQuote(current.name)}.${bracketQuote(original.name)}`;
        statements.push(`EXEC sp_rename ${sqlUnicodeLiteral(fullColumnName)}, ${sqlUnicodeLiteral(nextName)}, N'COLUMN'`);
      }
      else {
        statements.push(`ALTER TABLE ${tableName} RENAME COLUMN ${quoteIdentifier(current.provider, original.name)} TO ${quoteIdentifier(current.provider, nextName)}`);
      }
    }

    if (typeChanged || nullableChanged) {
      if (current.provider === 'sqlserver') {
        statements.push(`ALTER TABLE ${tableName} ALTER COLUMN ${quoteIdentifier(current.provider, resolvedName)} ${nextType}${draft.isNullable ? ' NULL' : ' NOT NULL'}`);
      }
      else if (current.provider === 'postgresql') {
        if (typeChanged) {
          statements.push(`ALTER TABLE ${tableName} ALTER COLUMN ${quoteIdentifier(current.provider, resolvedName)} TYPE ${nextType}`);
        }

        if (nullableChanged) {
          statements.push(`ALTER TABLE ${tableName} ALTER COLUMN ${quoteIdentifier(current.provider, resolvedName)} ${draft.isNullable ? 'DROP' : 'SET'} NOT NULL`);
        }
      }
    }

    statements.push(...buildCommentStatements(current.provider, current.schema, current.name, resolvedName, original.comment, draft.comment));
  }

  /* 生成 DROP COLUMN 语句（放在末尾，先完成其它 ALTER 操作） */
  for (const droppedName of droppedColumns.value) {
    const original = current.columns.find((column) => column.name === droppedName);
    if (!original) {
      continue;
    }

    if (original.isPrimaryKey) {
      throw new Error(`字段 ${droppedName} 是主键，无法删除。`);
    }

    statements.push(`ALTER TABLE ${tableName} DROP COLUMN ${quoteIdentifier(current.provider, droppedName)}`);
  }

  return statements.join(';' + '\n');
}

