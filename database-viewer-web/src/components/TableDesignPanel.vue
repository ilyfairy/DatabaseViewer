  const value = nextValue.trim();
  const mode = typeEditorMode(column);
  if (mode === 'length' || mode === 'length-or-max') {
    column.length = value;
  }
  else if (mode === 'precision-scale') {
    column.precision = value;
  }
  else if (mode === 'precision') {
    column.precision = value;
  }
  else if (mode === 'scale') {
    column.scale = value;
  }

  syncTypeInput(column);
}

