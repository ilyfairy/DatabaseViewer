  // eslint-disable-next-line no-useless-assignment
  let sql = '';
  try {
    sql = buildAlterSql();
    if (!sql.trim()) {
      return;
    }
  }
  catch (error) {
    store.showNotice('warning', error instanceof Error ? error.message : '表设计校验失败');
    return;
